function getHexaColor(color){
    hexa = null;
    switch(color){
        case "white":
        hexa= "#FFFFFF";
        break;
        case "black":
        hexa= "#000000";
        break;
        case "blue":
        hexa= "#0b24fb";
        break;
        case "green":
        hexa= "#0e7e12";
        break;
        case "yellow":
        hexa= "#fffd38";
        break;
        case "pink":
        hexa= "#fec1cc";
        break;
} console.log(hexa);
}
getHexaColor("pink");

